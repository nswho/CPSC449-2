%definitions
hasParent(A, B) :-
    parent(B, A).

hasCommonName(N, C) :-
    hasCompoundName(G, S, N), commonName(S, C);
    commonName(N, C).

hasCommonName(G, S, C) :-
    hasParent(S, G), commonName(S, C).

hasSciName(C, N) :-
    order(N), hasCommonName(N, C);
    family(N), hasCommonName(N, C);
    genus(N), hasCommonName(N, C);
    hasCompoundName(G, S, N), hasCommonName(S, C).

hasCompoundName(G, S, N) :-
    compoundName(G, S, N).

isaStrict(A, B) :-
    hasParent(B, A);
    hasParent(A, B).

%isa(?A, ?B).

synonym(A,B) :-
    hasCommonName(B, A);
    hasCommonName(A, B).
    

